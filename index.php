        <h2>Protect your IP with Tor</h2>

        <div class="content">
            <p>
                Communicator uses by default the TOR network, seamlessly. There is nothing extra to install, it’s built in to Communicator. You can
                optionally use p2p direct communications if you don’t want to use TOR. Using TOR, even friend nodes cannot see your IP, which allows
                to
                safely and anonymously connect to unknown people. This is an excellent way to safely try the software while connecting to untrusted
                users.
            </p>
        </div>

        <br>

        <h2>Encryption</h2>

        <div class="content">
            <p>
                Everything you do with Unseen Communicator is encrypted using open-source libraries using a key you generate yourself. No one else
                keeps a
                copy of your keys, which provides security for your communications.
            </p>

            <p>
                Compare this to centralized services that use phone numbers, such as Skype, Telegram, WhatsApp, Signal, etc. Those services generate
                your
                keys and send them to you. You have no idea who else gets a copy of your keys. Additionally, everything is tied to your phone number.
                That
                is not private in any way.
            </p>

            <p>
                Another problem has become apparent; the deliberate censorship of telephone calls, voicemail and email communications. Messages are
                being
                “lost” as they travel through these communications systems. You don’t know what you are missing when you never receive the messages.
            </p>
        </div>

        <br>

        <h2>Distributed</h2>

        <div class="content">
            <p>
                Unseen Communicator has no central servers that can be shut down via traditional denial of service attacks. No user data is
                stored anywhere and there is no centralized administration of the network. You decide you want to participate and use and/or support
                the
                network with your computer. You can leave at any time. However, we recommend people download bootstrap relay node or supernode to help
                distributed network contacts to the network.
